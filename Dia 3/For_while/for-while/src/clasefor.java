public class clasefor {

    public static void main(String[] args) {

        System.out.println(
                "Clase for: es una estructura de control que permite repetir un bloque de código un número determinado de veces.");
        for (int i = -10; i < 5; i++) {
            System.out.println("Valor de i: " + i);

        }
    }

}
