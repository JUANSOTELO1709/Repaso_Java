public class clasedowhile {

    public static void main(String[] args) {

        System.out.println(
                "Clase do-while: es una estructura de control que permite repetir un bloque de código al menos una vez, y luego continuar repitiéndolo mientras una condición sea verdadera.");
        int i = 0;
        do {
            System.out.println("Valor de i: " + i); // imprime el valor de i
            i++; // incrementa el valor de i en 1
        } while (i < 5);
    }

}
