public class clasewhile {

    public static void main(String[] args) {

        System.out.println(
                "Clase while: es una estructura de control que permite repetir un bloque de código mientras una condición sea verdadera.");
        int i = 0;
        while (i < 5) {
            System.out.println("Valor de i: " + i); // imprime el valor de i
            i++; // incrementa el valor de i en 1
        }
    }

}
